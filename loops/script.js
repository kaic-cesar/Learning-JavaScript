
// WHILE -> Enquanto

document.writeln("UTILIZANDO WHILE:<br>")

var x = 0;

while(x <= 10) {
    console.log(x);
    document.writeln("<br> O valor de X é: " + x);

    // Aumenta o valor de X
    x++;
}


// FOR -> Para 
document.writeln("<br><br><br>UTILIZANDO FOR:<br>")

for(a = 0; a < 10; a++) {
    document.writeln("<br> O valor de A é: " + a);
}